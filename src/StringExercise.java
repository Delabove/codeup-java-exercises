public class StringExercise {

    public static void main(String[] args){


        String noEducation = "We don't need no education\nWe don't need no thought control";
        System.out.println(noEducation);

        String sInsideOfS = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(sInsideOfS);

        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }

}
